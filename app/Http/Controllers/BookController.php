     * Store a newly created resource in storage.
     */
    public function store(StoreBookRequest $request)
    {
        $book = Book::find('title', $request->validated('title'));

        if ($book) {
            $book->no_of_copies = $book->no_of_copies + 1;
            $book->save();
        }else {
            Book::create($request->validated());
        }

    }

